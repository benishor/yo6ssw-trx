#include "FirFilter.h"


FirFilter::~FirFilter() {
    delete[] h;
    delete[] z;
}

double FirFilter::process(double input) {
    // shift delay line
    for (int i = taps - 2; i >= 0; i--) {
        z[i + 1] = z[i];
    }
    z[0] = input;

    // calculate convolution result
    double accumulator = 0;
    for (int i = 0; i < taps; i++) {
        accumulator += z[i] * h[i];
    }

    return accumulator;
}

FirFilter::FirFilter(int t, double* coefficients)
        : taps(t) {
    z = new double[taps];
    h = new double[taps];

    for (int i = 0; i < taps; i++) {
        h[i] = coefficients[i];
    }
}

